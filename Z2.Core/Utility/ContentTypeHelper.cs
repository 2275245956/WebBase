        private static void dic_Add(string k, string v)
        {
            if (!dic.ContainsKey(k))
            {
                dic.Add(k, v);
            }
        }

        public static string ContentType(string Extension)
        {
            if (dic.ContainsKey(Extension))
            {
                return dic[Extension];
            }
            else
            {
                return "application/octet-stream";
            }
        }
